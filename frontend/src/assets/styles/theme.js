// Role-based theme colors
export const roleThemes = {
  ambulance: {
    primary: '#d32f2f', // Red
    secondary: '#ff5252',
  },
  police: {
    primary: '#1976d2', // Blue
    secondary: '#2196f3',
  },
  hospital: {
    primary: '#388e3c', // Green
    secondary: '#4caf50',
  },
  admin: {
    primary: '#7b1fa2', // Purple
    secondary: '#9c27b0',
  },
};

export const getRoleTheme = (role) => {
  const baseTheme = { ...theme };
  const roleColors = roleThemes[role] || roleThemes.admin;
  
  return createTheme({
    ...baseTheme,
    palette: {
      ...baseTheme.palette,
      primary: roleColors,
    },
  });
};

export default theme;